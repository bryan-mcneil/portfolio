#!/usr/bin/env node
/**
 * Bake simple-icons path data into lib/tech-icons.ts so the icon cloud needs
 * no runtime fetches and no files in the sync-managed public/media/.
 * Run manually when the icon list changes: node scripts/fetch-tech-icons.mjs
 */
import fs from "node:fs";
import path from "node:path";

const slugs = [
  "typescript",
  "javascript",
  "react",
  "nextdotjs",
  "php",
  "laravel",
  "nodedotjs",
  "express",
  "postgresql",
  "mysql",
  "sqlite",
  "tailwindcss",
  "flutter",
  "dart",
  "docker",
  "git",
  "github",
  "wordpress",
  "bootstrap",
  "jquery",
  "livewire",
  "alpinedotjs",
  "prisma",
  "googlegemini",
  "inertia",
];

// Microsoft pulled its brands from simple-icons; legacy Azure path kept here.
const extras = [
  {
    slug: "azure",
    title: "Microsoft Azure",
    hex: "#0078D4",
    path: "M5.483 21.3H24L14.025 4.013l-3.038 8.347 5.836 6.938L5.483 21.3zM13.23 2.7L6.105 8.677 0 19.253h5.505v.001L13.23 2.7z",
  },
];

const icons = [...extras];
for (const slug of slugs) {
  const res = await fetch(`https://cdn.simpleicons.org/${slug}`);
  if (!res.ok) {
    console.error(`skip ${slug}: HTTP ${res.status}`);
    continue;
  }
  const svg = await res.text();
  const hex = svg.match(/fill="(#[0-9A-Fa-f]{6})"/)?.[1];
  const title = svg.match(/<title>([^<]+)<\/title>/)?.[1];
  const d = svg.match(/<path d="([^"]+)"/)?.[1];
  if (!hex || !title || !d) {
    console.error(`skip ${slug}: unexpected SVG shape`);
    continue;
  }
  icons.push({ slug, title, hex, path: d });
}

const out = `// Generated by scripts/fetch-tech-icons.mjs. Do not edit by hand.

export interface TechIcon {
  slug: string;
  title: string;
  /** Brand color from simple-icons. */
  hex: string;
  /** SVG path data on a 24x24 viewBox. */
  path: string;
}

export const techIcons: TechIcon[] = ${JSON.stringify(icons, null, 2)};
`;

const dest = path.join(process.cwd(), "lib", "tech-icons.ts");
fs.writeFileSync(dest, out);
console.log(`Wrote ${icons.length} icons to lib/tech-icons.ts`);
